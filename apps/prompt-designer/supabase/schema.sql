execute function public.set_updated_at();

create or replace function public.get_or_create_author(
  author_name text,
  author_email text default null,
  author_team text default null
)
returns bigint
language plpgsql
as $$
declare
  resolved_id bigint;
begin
  select id
  into resolved_id
  from public.authors
  where lower(name) = lower(trim(author_name))
  limit 1;

  if resolved_id is not null then
    update public.authors
    set email = coalesce(author_email, email),
        team = coalesce(author_team, team)
    where id = resolved_id;

    return resolved_id;
  end if;

  insert into public.authors (name, email, team)
  values (trim(author_name), nullif(trim(author_email), ''), nullif(trim(author_team), ''))
  returning id into resolved_id;

  return resolved_id;
end;
$$;

create or replace function public.get_or_create_tag(tag_name text)
returns bigint
language plpgsql
as $$
declare
  resolved_id bigint;
begin
  select id
  into resolved_id
  from public.tags
  where lower(name) = lower(trim(tag_name))
  limit 1;

  if resolved_id is not null then
    return resolved_id;
  end if;

  insert into public.tags (name)
  values (trim(tag_name))
  returning id into resolved_id;

  return resolved_id;
end;
$$;

create or replace function public.compose_prompt(p_prompt_id bigint)
returns text
language sql
stable
as $$
  select coalesce(
    string_agg(
      trim(both from concat(component_label, E'\n', content)),
      E'\n\n'
      order by order_index, id
    ),
    ''
  )
  from public.prompt_components
  where prompt_id = p_prompt_id
    and coalesce(is_enabled, true);
$$;

create or replace function public.calculate_prompt_diff(old_prompt_id bigint, new_prompt_id bigint)
returns jsonb
language sql
stable
as $$
  select jsonb_build_object(
    'old_prompt_id', old_prompt_id,
    'new_prompt_id', new_prompt_id,
    'before', public.compose_prompt(old_prompt_id),
    'after', public.compose_prompt(new_prompt_id)
  );
$$;

create or replace view public.prompts_with_components as
select
  p.id as prompt_id,
  p.title,
  p.description,
  p.version,
  p.created_at,
  p.updated_at,
  a.name as author_name,
  a.team as author_team,
  coalesce(
    jsonb_agg(
      jsonb_build_object(
        'id', pc.id,
        'component_key', pc.component_key,
        'component_label', pc.component_label,
        'content', pc.content,
        'order_index', pc.order_index,
        'is_enabled', pc.is_enabled,
        'created_at', pc.created_at,
        'updated_at', pc.updated_at
      )
      order by pc.order_index, pc.id
    ) filter (where pc.id is not null),
    '[]'::jsonb
  ) as components
from public.prompts p
left join public.authors a on a.id = p.author_id
left join public.prompt_components pc on pc.prompt_id = p.id
group by
  p.id,
  p.title,
  p.description,
  p.version,
  p.created_at,
  p.updated_at,
  a.name,
  a.team;

create or replace view public.latest_prompts_consolidated as
select
  p.id,
  p.title,
  p.description,
  p.version,
  p.created_at,
  p.updated_at,
  a.name as author_name,
  a.team as author_team,
  public.compose_prompt(p.id) as consolidated_prompt,
  tags.tags,
  tools.tools,
  vars.variable_count
from public.prompts p
left join public.authors a on a.id = p.author_id
left join lateral (
  select coalesce(array_agg(t.name order by t.name), '{}'::text[]) as tags
  from public.prompt_tags pt
  join public.tags t on t.id = pt.tag_id
  where pt.prompt_id = p.id
) tags on true
left join lateral (
  select coalesce(array_agg(tl.name order by tl.name), '{}'::text[]) as tools
  from public.prompt_tools ptl
  join public.tools tl on tl.id = ptl.tool_id
  where ptl.prompt_id = p.id
    and coalesce(ptl.is_enabled, true)
) tools on true
left join lateral (
  select count(*)::integer as variable_count
  from public.prompt_variables pv
  where pv.prompt_id = p.id
) vars on true
where coalesce(p.is_active, true);
