-- Prompt Designer bootstrap schema for Supabase.
-- Run this file in the Supabase SQL editor for the project used by `apps/prompt-designer`.
-- If you do not want Supabase yet, set `VITE_DATA_PROVIDER=local` in `apps/prompt-designer/.env`.
-- RLS is intentionally not enabled here so the browser app can work during local development.

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create table if not exists public.authors (
  id bigint generated by default as identity primary key,
  name text not null,
  email text,
  team text,
  created_at timestamptz not null default timezone('utc', now()),
  constraint authors_name_key unique (name)
);

create table if not exists public.tags (
  id bigint generated by default as identity primary key,
  name text not null,
  created_at timestamptz not null default timezone('utc', now()),
  constraint tags_name_key unique (name)
);

create table if not exists public.tools (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  configuration jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  constraint tools_name_key unique (name)
);

create table if not exists public.prompts (
  id bigint generated by default as identity primary key,
  author_id bigint not null references public.authors (id) on delete restrict,
  title text not null,
  description text,
  version text not null,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.prompt_components (
  id bigint generated by default as identity primary key,
  prompt_id bigint not null references public.prompts (id) on delete cascade,
  component_key text not null,
  component_label text not null,
  content text not null default '',
  order_index integer not null default 0,
  is_enabled boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint prompt_components_prompt_id_component_key_key unique (prompt_id, component_key)
);

create table if not exists public.prompt_tags (
  prompt_id bigint not null references public.prompts (id) on delete cascade,
  tag_id bigint not null references public.tags (id) on delete cascade,
  primary key (prompt_id, tag_id)
);

create table if not exists public.prompt_tools (
  prompt_id bigint not null references public.prompts (id) on delete cascade,
  tool_id bigint not null references public.tools (id) on delete cascade,
  is_enabled boolean not null default true,
  primary key (prompt_id, tool_id)
);

create table if not exists public.prompt_variables (
  id bigint generated by default as identity primary key,
  prompt_id bigint references public.prompts (id) on delete cascade,
  variable_name text not null,
  variable_type text not null default 'text',
  description text,
  default_value text,
  is_required boolean not null default false,
  created_at timestamptz not null default timezone('utc', now())
);

create unique index if not exists prompt_variables_prompt_variable_name_idx
  on public.prompt_variables (prompt_id, variable_name)
  where prompt_id is not null;

create unique index if not exists prompt_variables_global_variable_name_idx
  on public.prompt_variables (variable_name)
  where prompt_id is null;

create table if not exists public.prompt_history (
  id bigint generated by default as identity primary key,
  prompt_id bigint not null references public.prompts (id) on delete cascade,
  previous_version_id bigint references public.prompts (id) on delete set null,
  change_type text not null,
  changed_by_id bigint references public.authors (id) on delete set null,
  change_message text,
  component_diffs jsonb,
  tags_added text[],
  tags_removed text[],
  tools_added text[],
  tools_removed text[],
  variables_added jsonb,
  variables_removed jsonb,
  variables_modified jsonb,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.information_sources (
  id bigint generated by default as identity primary key,
  author_id bigint not null references public.authors (id) on delete restrict,
  name text not null,
  description text,
  source_type text not null,
  content text,
  url text,
  metadata jsonb,
  is_active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint information_sources_source_type_check
    check (source_type in ('link', 'json', 'secret', 'markdown', 'text', 'file'))
);

create index if not exists prompts_updated_at_idx on public.prompts (updated_at desc);
create index if not exists prompt_components_prompt_id_order_index_idx on public.prompt_components (prompt_id, order_index);
create index if not exists prompt_tags_prompt_id_idx on public.prompt_tags (prompt_id);
create index if not exists prompt_tools_prompt_id_idx on public.prompt_tools (prompt_id);
create index if not exists prompt_variables_prompt_id_idx on public.prompt_variables (prompt_id);
create index if not exists information_sources_created_at_idx on public.information_sources (created_at desc);

